#include <stdint.h>
#include <unistd.h>
#include <stdio.h>
#include "../ninohash.h"


int main()
{
    char csprng_state[32];
    FILE *output = fopen("csprng.bin", "wb");
    for (long iteration = 0; iteration < (75000000000 / 32); iteration++)
    {
        nino256sum(csprng_state, 32, csprng_state);

        fwrite(csprng_state, 1, 32, output);
    }
    fclose(output);
}